////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE imports
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import { createLogger } from 'redux-logger';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE utils
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const logger = createLogger({
  actionTransformer: (action: any) => ({ ...action, type: String(action.type) }),
  collapsed: false,
  level: 'info',
  logger: console,
  // @ts-ignore
  predicate: () => process.env.NODE_ENV !== 'production',
});

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE export
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export default logger;

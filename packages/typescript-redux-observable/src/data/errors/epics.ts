////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE Imports
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import { mergeMap } from 'rxjs/operators';
import { ofType, combineEpics } from 'redux-observable';
import { Observable, EMPTY } from 'rxjs';
import { ERRORS_ACTION_TYPES, TErrorsActionType } from './types';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE Epics
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const errorsTypesValues = Object.values(ERRORS_ACTION_TYPES);

const errorHendlerEpick = (action$: Observable<TErrorsActionType>) => {
  return action$.pipe(
    ofType(...errorsTypesValues),
    mergeMap((_: TErrorsActionType) => {
      return EMPTY;
    }),
  );
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE Exports
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export default combineEpics(
  ...Object.values({
    errorHendlerEpick,
  }),
);

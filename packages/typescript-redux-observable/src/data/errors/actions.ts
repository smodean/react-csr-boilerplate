////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE imports
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import { action } from 'typesafe-actions';
import { ERRORS_ACTION_TYPES } from './types';
import { AxiosError } from 'axios';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NOTE actions
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export const onLoginError = (error: AxiosError) => action(ERRORS_ACTION_TYPES.LOGIN_ERROR, undefined, undefined, error);
export const resetOnLoginError = (error = null) =>
  action(ERRORS_ACTION_TYPES.RESET_LOGIN_ERROR, undefined, undefined, error);
